package Package1;

public class Logical_Bitwise_Class {

	public static void main(String[] args) {
		/*
		 * Logical and &&, logical or ||, Logical not !	 
		 */
		
		boolean a=false;
		boolean b=true;
		int i1 =60;
		int i2=2;
		System.out.println(a && b);
		System.out.println(a || b);
		System.out.println(!a);
		System.out.println(i1 & i2);
	}
}
